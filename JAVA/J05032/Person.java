package J05032;

public class Person {
    
    private String name;
    private String dob;

    public Person(String name, String dob) {
        this.name = name;
        this.dob = dob;
    }
    
    public String getName() {
        return this.name;
    }
    
    public String getDob() {
        return this.dob;
    }
}

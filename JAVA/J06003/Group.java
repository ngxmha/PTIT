package J06003;

public class Group {
    
    private int sttGroup;
    private String name;

    public Group(int sttGroup, String name) {
        this.sttGroup = sttGroup;
        this.name = name;
    }
    
    public int getSttGroup() {
        return sttGroup;
    }

    public String getName() {
        return name;
    }
    
    
}

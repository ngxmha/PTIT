package J07060;

public class MonThi {
    
    private String id;
    private String name;
    private String form;

    public MonThi(String id, String name, String form) {
        this.id = id;
        this.name = name;
        this.form = form;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
    
}